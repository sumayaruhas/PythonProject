# using for loop in python

# printing out each element in a list

letters = ['a' , 'b' , 'c' , 'd' , 'e' , 'f' , 'g']

for x in letters:
    print(x)
    
# printing out each element in a string

for x in 'python':
    print(x)

for x in 'elephant':
    print(x)
    
# printing out in a range
for x in range(20):
    print(x)

# printing out in a range with a start and end

for x in range(10 , 20):
    print(x)

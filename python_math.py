# doing mathematics

import math # import math module

x = 2 + 2 # addition
print(x)

y = 2 - 2 # subtraction
print(y)

c = 2 * 3 # multiplication
print(c)

d = 3 / 2 # division
print(d)

x = max(1 , 2 , 3) # maximum
y = min(1 , 2 , 3) # minimum
print(x)
print(y)

d = abs(-32) # absolute value
print(d)

a = pow(2 , 3) # power
print(a)

e = abs(-6.7)
print(e)

a = math.sqrt(32) # square root
print(a)

x = math.ceil(2.5) # rounds number upwards
print(x)

y = math.floor(2.5) # rounds number downwards
print(y)

x = math.pi # value of pi
print(x)

# finding out maximum number
print(max(1 , 3 , 6 , 3 , 7 , 9 , 2 , 5))
#finding out minimum number
print(min(1 , 3 , 6 , 3 , 7 , 9 , 2 , 5))

# printing a complete number

print(round(5 / 6 * pow(2 , 3) + 6))

# trying to find out factorial of a number

print(factorial(5))





# trying to find the index of a character in a string

sentence = "this is my first project"

print(sentence.index("m"))
print(sentence.index("f"))
print(sentence.index("r"))

# replacing words in a string

print(sentence.replace("first","second"))
print(sentence.replace("this","that"))

